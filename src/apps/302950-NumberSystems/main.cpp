

#include <iostream>


int main(){
   
   int decimalNumber1 = 15; // Decimal
   int octalNumber2 = 017; // Octal
   int hexadecimalNumber3 = 0x0F; // Hexadecimal
   int binaryNumber4 = 0b00001111; // Binary

   std::cout << "decimalNumber1 : " << decimalNumber1 << std::endl;
   std::cout << "octalNumber2 : " << octalNumber2 << std::endl;
   std::cout << "hexadecimalNumber3 : " << hexadecimalNumber3 << std::endl;
   std::cout << "binaryNumber4 : " << binaryNumber4 << std::endl;
   
    return 0;
}